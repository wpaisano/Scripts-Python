#Faça um programa que leia algo pelo teclado e mostre na tela o seu tipo primitivo e todas as informações possíveis sobre ele.

n1 = input('Informe um número e descubra tudo sobre ele: ')
print(n1.isalnum())
print(n1.isalpha())
print(n1.isascii())
print(n1.isdecimal())
print(n1.isdigit())
print(n1.islower())
print(n1.isnumeric())
print(n1.isupper())
print(n1.islower())
print(n1.isidentifier())